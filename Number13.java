
public class Number13 {

	public static void main(String[] args) {
		// Using the modulo operator to return the remainder of a division operation
		// I created this as part of my research into Java's arithmetic operator suite
	
		double x = 11.52; 
		double y = 2045.97; 
		
		System.out.println(x + " modulus 3 = " + x % 3); 
		System.out.println(y + " modulus 3 = " + y % 3);

	}

}
